		data() {
			return {
				id: '',
				type: '',
			
			
				ro:{
					peixunmingcheng : false,
					peixunleixing : false,
					peixuntupian : false,
					fabushijian : false,
					peixundidian : false,
					peixunneirong : false,
					zhanghao : false,
					xingming : false,
				},
			
				ruleForm: {
					peixunmingcheng: '',
					peixunleixing: '',
					peixuntupian: '',
					fabushijian: '',
					peixundidian: '',
					peixunneirong: '',
					zhanghao: '',
					xingming: '',
				},
				peixunleixingOptions: [],

				rules: {
					peixunmingcheng: [
					],
					peixunleixing: [
						{ required: true, message: '培训类型不能为空', trigger: 'blur' },
					],
					peixuntupian: [
					],
					fabushijian: [
					],
					peixundidian: [
					],
					peixunneirong: [
					],
					zhanghao: [
					],
					xingming: [
					],
				},
			};
		},
		props: ["parent"],
		computed: {



		},
		components: {
		},
		created() {
			this.ruleForm.fabushijian = this.getCurDate()
		},
		methods: {
			// 下载
			download(file){
				window.open(`${file}`)
			},
			// 初始化
			init(id,type) {
				if (id) {
					this.id = id;
					this.type = type;
				}
				if(this.type=='info'||this.type=='else'||this.type=='msg'){
					this.info(id);
				}else if(this.type=='logistics'){
					for(let x in this.ro) {
						this.ro[x] = true
					}
					this.logistics=false;
					this.info(id);
				}else if(this.type=='cross'){
					var obj = this.$storage.getObj('crossObj');
					for (var o in obj){
						if(o=='peixunmingcheng'){
							this.ruleForm.peixunmingcheng = obj[o];
							this.ro.peixunmingcheng = true;
							continue;
						}
						if(o=='peixunleixing'){
							this.ruleForm.peixunleixing = obj[o];
							this.ro.peixunleixing = true;
							continue;
						}
						if(o=='peixuntupian'){
							this.ruleForm.peixuntupian = obj[o];
							this.ro.peixuntupian = true;
							continue;
						}
						if(o=='fabushijian'){
							this.ruleForm.fabushijian = obj[o];
							this.ro.fabushijian = true;
							continue;
						}
						if(o=='peixundidian'){
							this.ruleForm.peixundidian = obj[o];
							this.ro.peixundidian = true;
							continue;
						}
						if(o=='peixunneirong'){
							this.ruleForm.peixunneirong = obj[o];
							this.ro.peixunneirong = true;
							continue;
						}
						if(o=='zhanghao'){
							this.ruleForm.zhanghao = obj[o];
							this.ro.zhanghao = true;
							continue;
						}
						if(o=='xingming'){
							this.ruleForm.xingming = obj[o];
							this.ro.xingming = true;
							continue;
						}
					}
				}
				// 获取用户信息
				this.$http({
					url: `${this.$storage.get('sessionTable')}/session`,
					method: "get"
				}).then(({ data }) => {
					if (data && data.code === 0) {
						var json = data.data;
						if(((json.zhanghao!=''&&json.zhanghao) || json.zhanghao==0) && this.$storage.get("role")!="管理员"){
							this.ruleForm.zhanghao = json.zhanghao
							this.ro.zhanghao = true;
						}
						if(((json.xingming!=''&&json.xingming) || json.xingming==0) && this.$storage.get("role")!="管理员"){
							this.ruleForm.xingming = json.xingming
							this.ro.xingming = true;
						}
					} else {
						this.$message.error(data.msg);
					}
				});
				this.$http({
					url: `option/peixunleixing/peixunleixing`,
					method: "get"
				}).then(({ data }) => {
					if (data && data.code === 0) {
						this.peixunleixingOptions = data.data;
					} else {
						this.$message.error(data.msg);
					}
				});
			
			},
			// 多级联动参数

			info(id) {
				this.$http({
					url: `peixunxinxi/info/${id}`,
					method: "get"
				}).then(({ data }) => {
					if (data && data.code === 0) {
						this.ruleForm = data.data;
						//解决前台上传图片后台不显示的问题
						let reg=new RegExp('../../../upload','g')//g代表全部
						this.ruleForm.peixunneirong = this.ruleForm.peixunneirong.replace(reg,'../../../django0bbzy4mb/upload');
					} else {
						this.$message.error(data.msg);
					}
				});
			},

			// 提交
			async onSubmit() {
					if(this.ruleForm.peixuntupian!=null) {
						this.ruleForm.peixuntupian = this.ruleForm.peixuntupian.replace(new RegExp(this.$base.url,"g"),"");
					}
					var objcross = this.$storage.getObj('crossObj');
					await this.$refs["ruleForm"].validate(async valid => {
						if (valid) {
							if(this.type=='cross'){
								var statusColumnName = this.$storage.get('statusColumnName');
								var statusColumnValue = this.$storage.get('statusColumnValue');
								if(statusColumnName!='') {
									var obj = this.$storage.getObj('crossObj');
									if(statusColumnName && !statusColumnName.startsWith("[")) {
										for (var o in obj){
											if(o==statusColumnName){
												obj[o] = statusColumnValue;
											}
										}
										var table = this.$storage.get('crossTable');
										await this.$http({
											url: `${table}/update`,
											method: "post",
											data: obj
										}).then(({ data }) => {});
									}
								}
							}
							
							await this.$http({
								url: `peixunxinxi/${!this.ruleForm.id ? "save" : "update"}`,
								method: "post",
								data: this.ruleForm
							}).then(async ({ data }) => {
								if (data && data.code === 0) {
									this.$message({
										message: "操作成功",
										type: "success",
										duration: 1500,
										onClose: () => {
											this.parent.showFlag = true;
											this.parent.addOrUpdateFlag = false;
											this.parent.peixunxinxiCrossAddOrUpdateFlag = false;
											this.parent.search();
											this.parent.contentStyleChange();
										}
									});
								} else {
									this.$message.error(data.msg);
								}
							});
						}
					});
			},
			// 获取uuid
			getUUID () {
				return new Date().getTime();
			},
			// 返回
			back() {
				this.parent.showFlag = true;
				this.parent.addOrUpdateFlag = false;
				this.parent.peixunxinxiCrossAddOrUpdateFlag = false;
				this.parent.contentStyleChange();
			},
			peixuntupianUploadChange(fileUrls) {
				this.ruleForm.peixuntupian = fileUrls;
			},
		}
	};
</script>
