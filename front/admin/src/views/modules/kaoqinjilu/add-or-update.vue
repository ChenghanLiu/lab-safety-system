					fengmian: [
					],
					yuefen: [
					],
					chuqintianshu: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					queqintianshu: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					chidaotianshu: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					zaotuitianshu: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					qingjiatianshu: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					zhanghao: [
					],
					xingming: [
					],
					yuangongzhanghao: [
					],
					yuangongxingming: [
					],
					beizhu: [
					],
				},
			};
		},
		props: ["parent"],
		computed: {



		},
		components: {
		},
