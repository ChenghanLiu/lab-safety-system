	} from "@/utils/validate";
	export default {
		data() {
			var validateIntNumber = (rule, value, callback) => {
				if(!value){
					callback();
				} else if (!isIntNumer(value)) {
					callback(new Error("请输入整数"));
				} else {
					callback();
				}
			};
			return {
				id: '',
				type: '',
			
			
				ro:{
					biaoti : false,
					fengmian : false,
					yuefen : false,
					chuqintianshu : false,
					queqintianshu : false,
					chidaotianshu : false,
					zaotuitianshu : false,
					qingjiatianshu : false,
					zhanghao : false,
					xingming : false,
					yuangongzhanghao : false,
					yuangongxingming : false,
					beizhu : false,
				},
			
				ruleForm: {
					biaoti: '',
					fengmian: '',
					yuefen: '',
					chuqintianshu: '',
					queqintianshu: '',
					chidaotianshu: '',
