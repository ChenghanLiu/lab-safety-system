					],
					xinzidaiyu: [
					],
					gongzuodidian: [
					],
					zhaopinxiangqing: [
					],
					zhanghao: [
					],
					xingming: [
					],
					storeupnum: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
				},
			};
		},
		props: ["parent"],
		computed: {



		},
