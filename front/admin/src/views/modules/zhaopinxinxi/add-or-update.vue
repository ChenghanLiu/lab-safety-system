		data() {
			var validateIntNumber = (rule, value, callback) => {
				if(!value){
					callback();
				} else if (!isIntNumer(value)) {
					callback(new Error("请输入整数"));
				} else {
					callback();
				}
			};
			return {
				id: '',
				type: '',
			
			
				ro:{
					zhaopinmingcheng : false,
					zhaopinleixing : false,
					fengmian : false,
					gongsimingcheng : false,
					zhaopinyaoqiu : false,
